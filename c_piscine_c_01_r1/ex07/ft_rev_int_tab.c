void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int aux;
	int j;

	i = 0;
	j = size;
	while (i < size)
	{
		aux = tab[i];
		tab[i] = tab[j - 1];
		tab[j - 1] = aux;
		i++;
		j--;
		size--;
	}
}
